    public static MoveData getMove(string name)
    {
        MoveData result = null;
        int i = 1;
        while (result == null && i < moves.Length)
        {
            if (moves[i].getName() == name)
            {
                result = moves[i];
            }
            i += 1;
        }
        return result;
    }
